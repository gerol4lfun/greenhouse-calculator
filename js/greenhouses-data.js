/**
 * Получить путь к папке с фотографиями для конкретной теплицы
 * @param {string} greenhouseName - Название теплицы из калькулятора (например, "ТЕПЛИЦА БОЯРСКАЯ ЛЮКС 2.5М")
 * @returns {string|null} - Путь к папке с фотографиями или null
 */
function getGreenhousePhotosPath(greenhouseName) {
    for (const typeKey in GREENHOUSES_DATA) {
        const type = GREENHOUSES_DATA[typeKey];
        if (type.variants[greenhouseName]) {
            const variant = type.variants[greenhouseName];
            return `image/greenhouses/${type.folder}/${variant.folder}`;
        }
    }
    return null;
}

/**
 * Получить все фотографии для конкретной теплицы
 * @param {string} greenhouseName - Название теплицы из калькулятора
 * @returns {Array<string>} - Массив путей к фотографиям
 */
function getGreenhousePhotos(greenhouseName) {
    if (typeof window !== 'undefined' && window.GREENHOUSES_PHOTOS_LIST) {
        return window.GREENHOUSES_PHOTOS_LIST[greenhouseName] || [];
    }
    return [];
}

/**
 * Получить данные о типе теплицы по ID
 * @param {string} typeId - ID типа (arochnaya, kaplevidnaya, и т.д.)
 * @returns {Object|null} - Данные о типе теплицы
 */
function getGreenhouseTypeData(typeId) {
    return GREENHOUSES_DATA[typeId] || null;
}

/**
 * Получить все типы теплиц
 * @returns {Array<Object>} - Массив типов теплиц
 */
function getAllGreenhouseTypes() {
    return Object.values(GREENHOUSES_DATA);
}

// Экспорт для использования в других файлах
if (typeof window !== 'undefined') {
    window.GREENHOUSES_DATA = GREENHOUSES_DATA;
    window.getGreenhousePhotosPath = getGreenhousePhotosPath;
    window.getGreenhousePhotos = getGreenhousePhotos;
    window.getGreenhouseTypeData = getGreenhouseTypeData;
    window.getAllGreenhouseTypes = getAllGreenhouseTypes;
}
